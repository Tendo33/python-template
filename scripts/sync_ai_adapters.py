def build_rule_file(
    root: Path, relative_path: str, description: str, globs: str, bullets: list[str]
) -> str:
    lines = [
        "---",
        f"description: {description}",
        f"globs: {globs}",
        "---",
        GENERATED_MARKER,
    ]
    lines.extend(f"- {bullet}" for bullet in bullets)
    return "\n".join(lines) + "\n"


def build_agents_rule(root: Path, relative_path: str, bullets: list[str]) -> str:
    return (
        dedent(
            f"""\
        ---
        trigger: always_on
        ---

        {GENERATED_MARKER}
        """
        )
        + "".join(f"\n- {bullet}" for bullet in bullets)
        + "\n"
    )


def build_skill(
    root: Path,
    relative_path: str,
    name: str,
    description: str,
    title: str,
    bullets: list[str],
) -> str:
    lines = [
        "---",
        f"name: {name}",
        f"description: {description}",
        "---",
        GENERATED_MARKER,
        f"# {title}",
        "",
    ]
    lines.extend(f"- {bullet}" for bullet in bullets)
    return "\n".join(lines) + "\n"


