from textwrap import dedent

GENERATED_MARKER = "<!-- Generated by scripts/sync_ai_adapters.py -->"


def get_repo_root() -> Path:
    return Path(__file__).resolve().parent.parent


def rel_link(file_path: Path, target: str) -> str:
    return os.path.relpath(target, start=file_path.parent).replace(os.sep, "/")


def markdown_link(file_path: Path, target: str, label: str) -> str:
    return f"[{label}]({rel_link(file_path, target)})"


def build_agents_md(root: Path) -> str:
    path = root / "AGENTS.md"
    start_here = markdown_link(path, "ai_docs/START_HERE.md", "ai_docs/START_HERE.md")
    index = markdown_link(path, "ai_docs/INDEX.md", "ai_docs/INDEX.md")
    verification = markdown_link(
        path, "ai_docs/reference/verification.md", "ai_docs/reference/verification.md"
    )
    return dedent(
        f"""\
        # Project Agent Entrypoint

        {GENERATED_MARKER}

        This file is the cross-tool entrypoint for this repository.

        ## Read order

        1. Start at {start_here}
        2. Use {index} to choose the right task path
        3. Use {verification} before claiming completion

        ## Working rules

        - Treat `ai_docs/` as the only detailed source of truth
        - Read `current/` before `standards/` and `workflows/`
        - Keep changes minimal, typed, and explicit
        - Update docs whenever behavior, structure, scripts, adapters, or public APIs change
        """
    )


